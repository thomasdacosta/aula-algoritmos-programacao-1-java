package edu.senac.aula04;

public class Variaveis {

	public static void main(String[] args) {
		String nome = "Bruce Wayne";
		int ano;
		double numeroPi;
		float salario;
		long cpf;
		boolean ligado;
		short idade;
		char letra;

		ano = 2022;
		numeroPi = 3.14159265d;
		salario = 768.35f;
		cpf = 43431611028l;
		ligado = true;
		idade = 21;
		letra = 'A';

		System.out.println("String: " + nome);
		System.out.println("int: " + ano);
		System.out.println("double: " + numeroPi);
		System.out.println("float: " + salario);
		System.out.println("long: " + cpf);
		System.out.println("boolean: " + ligado);
		System.out.println("short: " + idade);
		System.out.println("char: " + letra);
	}

}
